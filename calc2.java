public class calc2 {
	public double add(float x, float y) {
		double z;
		z = x + y;
		return z;
	}
	
	public double subtract(float x, float y) {
		double z;
		z = x - y;
		return z;
	}
	
	public double multiply(float x, float y) {
		double z;
		z = x * y;
		return z;
	}
	
	public double divide(float x, float y) {
		double z;
		z = x / y;
		return z;
	}
	
	public int mod(int x, int y) {
		int z;
		z = x % y;
		return z;
	}
}